from enum import Enum


class OcrType(Enum):
    """
    Enumeration for OCR (Optical Character Recognition) types.
    
    Attributes:
        STANDARD: Use the original OCR included in the dataset.
        GPT: Use the OCR generated by ChatGPT-4o.
        NONE: Use no OCR.
    """
    STANDARD = 1
    GPT = 2
    NONE = 3


class DescriptionType(Enum):
    """
    Enumeration for Description types.
    
    Attributes:
        KOSMOS: Use image descriptions generated by KOSMOS-2.
        GPT: Use image descriptions generated by ChatGPT-4o.
        NONE: Use no descriptions.
    """
    KOSMOS = 1
    GPT = 2
    NONE = 3
